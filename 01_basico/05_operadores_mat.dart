/**
 * Un operador es un simbolo que le dice al compilador que debe  realizar
 * una tarea matematica, logica o relacional
 * y debe de producir un resultado.
 */

main() {
int a = 10 + 5;   // + 15

a = 20 - 10; // - 10

a = 10 * 2; // * 20

double b = 10 / 2;  //  / 5

b = 10.0 % 3; //   % 1 = el sobrante de la division sintetica

b = -b;  // este operador es usado para cambiar el valor de la expresion

int c = 10 ~/ 3; //  ~/ 3 Division comun y corriente pero el valor observado es la parte entera 

int d = 1; 
d ++;   /// ++ es el operador para aumentar el valor de la variable, el valor en este ejemplo seria "2"
d --;   /// -- es el operador para disminuir el valor de la variable, el valor en este ejemplo seria "1"
 
 d += 2; /// += es el operador que utilizaria para aumentar en la cantidad que deseo, el valor en este ejemplo seria "3"
 d -= 2; /// -= es el operador que utilizaria para disminuir en la cantidad que deseo, el valor en este ejemplo seria "1"(estoy restando 2 al ejemplo en la linea precedente)

  
}